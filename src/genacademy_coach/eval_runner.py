DEFAULT_WRONG_ANSWER = "I am not sure; I think it just memorizes previous tokens."
INFRASTRUCTURE_REFUSAL_OBSERVATIONS = {STRUCTURED_OUTPUT_FAILURE_REASON}
REFUSAL_REASON_CODES = {
    STRUCTURED_OUTPUT_FAILURE_REASON: "structured_output_failure",
    "no citeable course corpus found": "no_citeable_course_corpus",
    "agent chose re_explain_differently without changing strategy": "repeated_reexplain_strategy",
    "agent displayed a check question that was not generated by the grounded tool": (
        "ungrounded_check_question"
    ),
    "grounded check citation was not present in retrieved spans": "grounded_check_citation_missing",
    "agent response was not faithful to retrieved citation text": "unfaithful_response",
    "agent response had no retrieved citation_ids": "missing_retrieved_citations",
}


def _safe_filename_part(value: str) -> str:
    return re.sub(r"[^A-Za-z0-9_.-]+", "-", value).strip("-") or "run"


def resolve_query(case: GoldenCase, scenario_index: dict[str, str]) -> str:
    if case.cloud_safe and case.user_query:
        return case.user_query
    source_ref = case.source_ref or ""
    if ":" not in source_ref:
        raise ValueError(
            f"{case.case_id}: cannot resolve query "
            "(no inline user_query and no parseable source_ref)"
        )
    scenario_id = source_ref.split(":", 1)[1]
    if scenario_id not in scenario_index:
        raise ValueError(
            f"{case.case_id}: source_ref scenario {scenario_id} not found in seed/dev scenarios"
        )
    return scenario_index[scenario_id]


def _model_id(foundation: Any) -> str:
    rag_settings = getattr(foundation, "rag_settings", None)
    return (getattr(rag_settings, "gen_model", None) if rag_settings is not None else None) or (
        DEFAULT_NEBIUS_MODEL
    )


def _reset_trace(settings: Any, session_id: str) -> None:
    trace_path = Path(settings.trace_dir) / f"{session_id}.jsonl"
    if trace_path.exists():
        trace_path.unlink()


def _trace_rows(trace_path: str | Path):
    path = Path(trace_path)
    return load_trace(path) if path.exists() else []


def _sum_float_dicts(rows: list[dict[str, float]]) -> dict[str, float]:
    totals: dict[str, float] = {}
    for row in rows:
        for key, value in row.items():
            totals[key] = totals.get(key, 0.0) + float(value)
    return dict(sorted(totals.items()))


def _sum_int_dicts(rows: list[dict[str, int]]) -> dict[str, int]:
    totals: dict[str, int] = {}
    for row in rows:
        for key, value in row.items():
            totals[key] = totals.get(key, 0) + int(value)
    return dict(sorted(totals.items()))


def _ranked_retrieval_ids(foundation: Any, query: str) -> list[str]:
    return [str(row.get("chunk_id", "")) for row in foundation.retrieve(query)]


def _is_infrastructure_refusal(response: Any) -> bool:
    return (
        response.next_action == "refuse_escalate"
        and response.observation in INFRASTRUCTURE_REFUSAL_OBSERVATIONS
    )


def _refusal_reason_code(response: Any) -> str | None:
    if response.next_action != "refuse_escalate":
        return None
    return REFUSAL_REASON_CODES.get(str(response.observation), "agent_refusal")


def _grade_summary(runtime: Any) -> dict[str, Any]:
    grade = getattr(runtime, "last_grade", None)
    if grade is None:
        return {
            "grade_scorer_version": None,
            "grade_literal_match_count": 0,
            "grade_semantic_match_count": 0,
            "grade_missing_keyword_count": 0,
            "grade_semantic_decisive": False,
        }
    modes = dict(getattr(grade, "matched_keyword_modes", {}) or {})
    semantic_match_count = sum(
        1 for mode in modes.values() if mode == "semantic_alias"
    )
    return {
        "grade_scorer_version": getattr(grade, "scorer_version", None),
        "grade_literal_match_count": sum(
            1 for mode in modes.values() if mode == "literal"
        ),
        "grade_semantic_match_count": semantic_match_count,
        "grade_missing_keyword_count": len(getattr(grade, "missing_keywords", []) or []),
        "grade_semantic_decisive": bool(
            getattr(grade, "correct", False) and semantic_match_count
        ),
    }


def _manifest_version(eval_dir: Path) -> str | None:
    manifest_path = eval_dir / "golden" / "golden_manifest.json"
    if not manifest_path.exists():
        return None
    payload = json.loads(manifest_path.read_text(encoding="utf-8"))
    version = payload.get("version")
    return str(version) if version is not None else None


def _sha256(path: Path | None) -> str | None:
    if path is None or not path.exists():
        return None
    return hashlib.sha256(path.read_bytes()).hexdigest()


def _trace_path_label(settings: Any, trace_path: str | Path) -> str:
    path = Path(trace_path)
    repo_root = getattr(settings, "repo_root", None)
    if repo_root is not None:
        try:
            return str(path.relative_to(Path(repo_root)))
        except ValueError:
            pass
    return path.name


def anchor_counterfactual(rows: list[dict[str, Any]]) -> dict[str, Any]:
    teachable = [row for row in rows if not row.get("refusal_expected")]
    lifts: list[str] = []
    regressions: list[str] = []
    blind_spots: list[str] = []
    fallback_lifts: list[str] = []
    fallback_regressions: list[str] = []
    actual_scores: list[float] = []
    anchor_scores: list[float] = []
    fallback_actual_scores: list[float] = []
    fallback_anchor_scores: list[float] = []

    for row in teachable:
        case_id = str(row.get("case_id") or "")
        expected_id = str(row.get("expected_citation_span_id") or "")
        predicted_ids = list(row.get("predicted_citation_ids") or [])
        anchor_id = str(row.get("answered_check_id") or "")
        anchor_present = bool(row.get("anchor_present_in_final_retrieved"))
        actual_f1 = citation_prf(predicted_ids, [expected_id])[2]
        actual_scores.append(actual_f1)
        if not anchor_present:
            blind_spots.append(case_id)
            continue

        anchor_f1 = citation_prf([anchor_id], [expected_id])[2]
        anchor_scores.append(anchor_f1)
        if anchor_f1 > actual_f1:
            lifts.append(case_id)
        elif anchor_f1 < actual_f1:
            regressions.append(case_id)

        if row.get("decision_source") == "python safety gate":
            fallback_actual_scores.append(actual_f1)
            fallback_anchor_scores.append(anchor_f1)
            if anchor_f1 > actual_f1:
                fallback_lifts.append(case_id)
            elif anchor_f1 < actual_f1:
                fallback_regressions.append(case_id)

    def _mean(values: list[float]) -> float:
        return sum(values) / len(values) if values else 0.0

    return {
        "teachable_n": len(teachable),
        "actual_citation_f1_mean": _mean(actual_scores),
        "anchor_present_n": len(anchor_scores),
        "anchor_citation_f1_mean_when_present": _mean(anchor_scores),
        "fallback_eligible_n": len(fallback_actual_scores),
        "fallback_actual_citation_f1_mean": _mean(fallback_actual_scores),
        "fallback_anchor_citation_f1_mean": _mean(fallback_anchor_scores),
        "lift_case_ids": lifts,
        "regression_case_ids": regressions,
        "blind_spot_case_ids": blind_spots,
        "fallback_lift_case_ids": fallback_lifts,
        "fallback_regression_case_ids": fallback_regressions,
    }


def score_golden_case(
    *,
    settings: Any,
    foundation: Any,
    case: GoldenCase,
    scenario_index: dict[str, str],
    run_id: str | None = None,
    session_factory: Any = CoachSession,
) -> dict[str, Any]:
    query = resolve_query(case, scenario_index)
    session_id = (
        f"golden-{_safe_filename_part(run_id)}-{case.case_id}"
        if run_id
        else f"golden-{case.case_id}"
    )
    _reset_trace(settings, session_id)
    session = session_factory(
        session_id=session_id,
        topic=query,
        settings=settings,
        foundation=foundation,
        profile=LearnerProfile(style="analogy", track_lens="code_heavy"),
    )
    session.start()
    second = session.respond(case.initial_wrong_answer or DEFAULT_WRONG_ANSWER)
    if case.expected_answer:
        correct_answer = case.expected_answer
    elif session.runtime.current_check is not None:
        correct_answer = session.runtime.current_check.expected_answer
    else:
        correct_answer = " ".join(case.expected_check_keywords)
    answered_check_id = (
        session.runtime.current_check.citation_id
        if session.runtime.current_check is not None
        else None
    )
    final = session.respond(correct_answer) if correct_answer else second

    rows = _trace_rows(final.trace_path)
    actual_tools = [tool for row in rows for tool in row.tool_calls]
    turn_latencies_ms = [row.latency_ms for row in rows]
    turn_agent_attempts = [int(row.agent_attempts) for row in rows]
    turn_retrieval_cache_hits = [int(row.retrieval_cache_hits) for row in rows]
    turn_tool_latencies_ms = [dict(row.tool_latencies_ms) for row in rows]
    turn_tool_call_counts = [dict(row.tool_call_counts) for row in rows]
    tool_latencies_ms = _sum_float_dicts(turn_tool_latencies_ms)
    tool_call_counts = _sum_int_dicts(turn_tool_call_counts)
    input_tokens = sum(row.input_tokens for row in rows)
    output_tokens = sum(row.output_tokens for row in rows)
    total_tokens = sum(row.total_tokens for row in rows)
    final_trace = rows[-1] if rows else None

    predicted_citations = set(final.response.citation_ids)
    expected_citations = (
        {case.expected_citation_span_id} if case.expected_citation_span_id else set()
    )
    citation_precision, citation_recall, citation_f1 = citation_prf(
        predicted_citations,
        expected_citations,
    )
    tool_scores = tool_match(actual_tools, case.expected_tools)
    ranked_ids = _ranked_retrieval_ids(foundation, query)
    final_action = final.response.next_action
    refusal = refusal_outcome(
        refusal_expected=case.refusal_expected,
        actual_next_action=final_action,
        infrastructure_error=_is_infrastructure_refusal(final.response),
    )
    grade_correct = bool(
        getattr(session.runtime, "last_grade", None)
        and session.runtime.last_grade.correct
    )
    grade_summary = _grade_summary(session.runtime)
    boundary_grade_citation_id = (
        session.runtime.last_grade.citation_id
        if getattr(session.runtime, "last_grade", None) is not None
        else None
    )
    post_final_check_id = (
        session.runtime.current_check.citation_id
        if session.runtime.current_check is not None
        else None
    )
    final_retrieved_ids = list(final_trace.retrieved_citation_ids) if final_trace else []
    provenance_by_role = {
        role: record.model_dump()
        for role, record in getattr(session.runtime, "provenance", {}).items()
    }
    if refusal == "infra_error":
        task_completion_pass = None
    else:
        task_completion_pass = (final_action == case.expected_next_action) and (
            case.refusal_expected or grade_correct
        )

    row: dict[str, Any] = {
        "case_id": case.case_id,
        "query_type": case.query_type,
        "concept": case.concept,
        "split": case.split,
        "cloud_safe": case.cloud_safe,
        "refusal_expected": case.refusal_expected,
        "expected_next_action": case.expected_next_action,
        "actual_next_action": final_action,
        "actual_strategy": final.response.strategy,
        "actual_tools": actual_tools,
        "expected_tools": case.expected_tools,
        "retrieved_citation_ids": final_retrieved_ids,
        "predicted_citation_ids": list(final.response.citation_ids),
        "answered_check_id": answered_check_id,
        "post_final_check_id": post_final_check_id,
        "boundary_grade_citation_id": boundary_grade_citation_id,
        "anchor_present_in_final_retrieved": bool(
            answered_check_id and answered_check_id in final_retrieved_ids
        ),
        "provenance_by_role": provenance_by_role,
        "teaching_provenance_span_id": (
            provenance_by_role.get("teaching", {}).get("span_id")
        ),
        "check_provenance_span_id": provenance_by_role.get("check", {}).get("span_id"),
        "final_provenance_span_id": provenance_by_role.get("final", {}).get("span_id"),
        "decision_source": final.response._decision_source,
        "refusal_reason_code": _refusal_reason_code(final.response),
        "final_trace_path": _trace_path_label(settings, final.trace_path),
        "expected_citation_span_id": case.expected_citation_span_id,
        "evidence_score": final_trace.evidence_score if final_trace else 0.0,
        "evidence_band": final_trace.evidence_band if final_trace else "stop",
        "faithfulness_ok": final_trace.faithfulness_ok if final_trace else None,
        "turns_used": len(rows),
        "turn_latencies_ms": turn_latencies_ms,
        "case_latency_ms": sum(turn_latencies_ms),
        "turn_agent_attempts": turn_agent_attempts,
        "agent_attempts": sum(turn_agent_attempts),
        "turn_retrieval_cache_hits": turn_retrieval_cache_hits,
        "retrieval_cache_hits": sum(turn_retrieval_cache_hits),
        "turn_tool_latencies_ms": turn_tool_latencies_ms,
        "turn_tool_call_counts": turn_tool_call_counts,
        "tool_latencies_ms": tool_latencies_ms,
        "tool_call_counts": tool_call_counts,
        "input_tokens": input_tokens,
        "output_tokens": output_tokens,
        "total_tokens": total_tokens,
        "model_id": _model_id(foundation),
        "task_completion_pass": task_completion_pass,
        "grade_correct": grade_correct,
        **grade_summary,
        "citation_precision": citation_precision,
        "citation_recall": citation_recall,
        "citation_f1": citation_f1,
        "tool_precision": float(tool_scores["precision"]),
        "tool_recall": float(tool_scores["recall"]),
        "tool_f1": float(tool_scores["f1"]),
        "retrieval_recall_at_5": recall_at_k(
            ranked_ids,
            case.expected_citation_span_id,
            k=5,
        ),
        "refusal_outcome": refusal,
    }
    if case.cloud_safe:
        row["user_query"] = query
        row["answer_text"] = final.response.learner_message
    return row


def _scenario_index(settings: Any) -> dict[str, str]:
    scenarios = []
    for split in ("seed", "dev"):
        scenarios.extend(load_scenarios(settings, split=split, limit=9999))
    return {row["scenario_id"]: row["question_text"] for row in scenarios}


def run_golden_eval(
    *,
    settings: Any,
    foundation: Any,
    cases: list[GoldenCase],
    tag: str,
    price_table: PriceTable,
    run_id: str | None = None,
    golden_cases_path: Path | None = None,
    session_factory: Any = CoachSession,
) -> dict[str, Any]:
    scenario_index = _scenario_index(settings)
    rows = [
        score_golden_case(
            settings=settings,
            foundation=foundation,
            case=case,
            scenario_index=scenario_index,
            run_id=run_id,
            session_factory=session_factory,
        )
        for case in cases
    ]
    metrics = aggregate(rows, price_table=price_table)
    filename_run = f"-{_safe_filename_part(run_id)}" if run_id else ""
    output_path = (
        Path(settings.eval_dir)
        / "runs"
        / f"golden-{_safe_filename_part(tag)}{filename_run}-{date.today():%Y%m%d}.json"
    )
    output_path.parent.mkdir(parents=True, exist_ok=True)
    payload = {
        "created": datetime.now(UTC).isoformat(),
        "tag": tag,
        "run_id": run_id,
        "n": len(rows),
        "golden_manifest_version": _manifest_version(Path(settings.eval_dir)),
        "golden_cases_sha256": _sha256(golden_cases_path),
        "metrics": metrics,
        "anchor_counterfactual": anchor_counterfactual(rows),
        "rows": rows,
    }
    output_path.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    return {**payload, "output_path": str(output_path)}
